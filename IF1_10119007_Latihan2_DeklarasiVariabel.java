public class IF1_10119007_Latihan2_DeklarasiVariabel {
public static void main(String[] args) {

	//Deklarasi Variable
	int nilaiint;
	final double PHI = 3.14;//konstanta overcase
	boolean nilaiLogika;
	char nilaiKarakter;

	//memberi nilai variabel
	nilaiint = 78;
	nilaiLogika = false;
	nilaiKarakter = 'D';

	//tampilan
	System.out.println();
	System.out.println("Isi variabel nilai = " + nilaiint);
	System.out.println("Isi variabel PHI = " + PHI);
	System.out.println("Isi variabel Logika = " + nilaiLogika);
	System.out.println("Isi variabel Karakter = " + nilaiKarakter);
}
}